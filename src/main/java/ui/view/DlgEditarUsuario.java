    public void actualizarUsuario() {
        String nombre = txtNombre.getText();
        String email = txtxEmail.getText();
        String pass = txtPass.getText();

        if (nombre.isEmpty() || email.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Los campos no pueden estar vac√≠os");
            return;
        }

        usuario.setNombre(nombre);
        usuario.setEmail(email);
        usuario.setPass(pass);

        user.editarUsuario(usuario.getId(), nombre, email, pass, "usuario");
        JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente");
    }

    public void setUsuario(Usuario user) {
        this.usuario = user; // guardamos ID y datos
        txtNombre.setText(user.getNombre());
        txtxEmail.setText(user.getEmail());
        txtPass.setText(user.getPass());
    }

